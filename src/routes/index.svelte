    <h1 class="font-semibold tracking-wide">Safe To Use, Open-Source Text Tools</h1>
    <p>
        Online text tools are a dime a dozen. Most of them haven't been redesigned since Will Smith
        was topping the music charts. Some are attempting to copy times square, with an ad on every
        free inch of the page. Maybe the worst offenders though, are the nefarious tools which
        consume the data you're expecting them to protect and storing/stealing it for their own
        benefit.
    </p>
    <h2 class="font-semibold tracking-wide">Why is SafeText.Tools Different?</h2>
    <ul>
        <li>
            We don't monetize the tools for profit. We do accept donations only to the point where
            our operating costs are covered. This means you won't see flashing ads, auto-play
            videos, or other seizure-inducing media.
        </li>
        <li>
            We don't send your text over the internet to ours or other servers. Any text you're
            processing on the site, stays on your machine.
        </li>
        <li>
            Our code is open-sourced on <a
                class="text-[#982ef7]"
                href="https://github.com/joshpierce/safetext.tools">github</a
            >, allowing you (if you want) to see the internal functionality of our tools. Guarantee
            that the tools aren't doing anything malicious with your data.
        </li>
        <li>
            We encourage people to file
            <a
                href="https://github.com/joshpierce/safetext.tools/issues/new?assignees=&labels=bug&template=bug-report.md&title=%5BBUG%5D%3A+Short+Description+Of+The+%F0%9F%90%9E"
                class="text-[#982ef7]">bug reports</a
            >
            and
            <a
                href="https://github.com/joshpierce/safetext.tools/issues/new?assignees=&labels=enhancement&template=feature-request.md&title=%5BFEATURE+REQUEST%5D%3A+Short+Description+of+the+Feature"
                class="text-[#982ef7]">feature requests</a
            > so we can continue to build awesome tools for you.
        </li>
    </ul>
